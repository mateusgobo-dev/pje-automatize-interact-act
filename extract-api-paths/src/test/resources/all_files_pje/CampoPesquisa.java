package br.com.infox.DAO;

public class CampoPesquisa {

	private String nome;
	private String valor;

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNome() {
		return nome;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public String getValor() {
		return valor;
	}

}
